export function buildGptIngestBrainUserPrompt(input: {
  memory: GptIngestMemoryInput;
  currentInput: string;
}) {
  return [
    buildGptIngestMemoryPrompt(input.memory),
    "",
    "## 本次管理员投喂内容",
    input.currentInput,
    "",
    "如果本次带附件，请把“当前附件”的正文、摘要或限制说明作为主要上下文；不要只围绕文件名猜测。若附件正文不足，请自然说明限制并给出下一步补充建议。",
    "",
    "请基于以上上下文继续完成同一 Agent 下的投喂大脑任务：先像 ChatGPT 一样自然回应管理员，再在 JSON 的 knowledgeDraft 字段生成可保存草稿。"
  ].join("\n");
}
