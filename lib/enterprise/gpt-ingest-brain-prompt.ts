    "主回复只围绕当前意图展开，不要把所有请求都写成同一套知识库报告。",
    "",
    "## 附件使用要求",
    "如果本次带附件，请把“当前附件”的正文、摘要或限制说明作为主要上下文；不要只围绕文件名猜测。若附件正文不足，请自然说明限制并给出下一步补充建议。",
    "如果当前附件里有 extractedText / slideTexts / pageSummaries，你必须明显结合这些内容分析。",
    "如果当前附件 parseStatus=parsed 或 partial，必须在 replyMarkdown 中体现你确实读到了正文片段，并把 PPT / Word / PDF 的正文信息纳入知识库拆分、用户端调用策略和合规风控。",
